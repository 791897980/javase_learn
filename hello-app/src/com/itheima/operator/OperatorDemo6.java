package com.itheima.operator;

public class OperatorDemo6 {
    public static void main(String[] args) {
        //目标： 学会使用条件运算符
        int a = 10;
        int b = 10;
        boolean rs = a == b;
        System.out.println(rs);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
        int i = 10;
        int j = 5;
    }
}
